int	ft_isalnum(int c) // check for alphanumerical character
{
	if (c >= 48 && c <= 57)
		return (1);
	else if ((c >= 97 && c <= 122) || (c >= 65 && c <= 90))
		return (1);
	else
		return (0);
}
